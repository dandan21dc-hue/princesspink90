#!/usr/bin/env node
/**
 * Lightweight coverage + test summary for the reminder-cron-activation path.
 *
 * Runs `src/lib/reminderCronActivation.test.ts` with the v8 coverage provider
 * scoped to the hook it exercises and writes a human-readable Markdown report
 * to `docs/testing/reminder-cron-activation.md`. Track that file over time to
 * spot regressions: a shrinking test list, dropping line/branch %, or a newly
 * uncovered line range all show up as a small, reviewable diff.
 *
 * Usage:
 *   bun run report:reminder-cron        # regenerate the committed report
 */
import { spawnSync } from 'node:child_process'
import { mkdirSync, readFileSync, writeFileSync, rmSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const TEST_FILE = 'src/lib/reminderCronActivation.test.ts'
const HOOK_FILE = 'src/routes/api/public/hooks/health-screening-reminders.ts'
const OUT_DIR = join(ROOT, 'docs/testing')
const OUT_FILE = join(OUT_DIR, 'reminder-cron-activation.md')
const COV_DIR = join(ROOT, '.tmp/rca-cov')
const TEST_JSON = join(ROOT, '.tmp/rca-tests.json')

mkdirSync(OUT_DIR, { recursive: true })
mkdirSync(dirname(TEST_JSON), { recursive: true })
rmSync(COV_DIR, { recursive: true, force: true })

const result = spawnSync(
  'bunx',
  [
    'vitest',
    'run',
    TEST_FILE,
    '--coverage',
    '--coverage.provider=v8',
    '--coverage.reporter=json-summary',
    `--coverage.include=${HOOK_FILE}`,
    `--coverage.reportsDirectory=${COV_DIR}`,
    '--reporter=json',
    `--outputFile=${TEST_JSON}`,
  ],
  { cwd: ROOT, stdio: ['ignore', 'pipe', 'pipe'], encoding: 'utf8' },
)

if (result.status !== 0) {
  process.stderr.write(result.stdout ?? '')
  process.stderr.write(result.stderr ?? '')
  process.exit(result.status ?? 1)
}

const tests = JSON.parse(readFileSync(TEST_JSON, 'utf8'))
const cov = JSON.parse(readFileSync(join(COV_DIR, 'coverage-summary.json'), 'utf8'))

const suite = tests.testResults?.[0]
const assertions = suite?.assertionResults ?? []
const passed = assertions.filter((a) => a.status === 'passed').length
const failed = assertions.filter((a) => a.status === 'failed').length
const skipped = assertions.filter((a) => a.status === 'pending' || a.status === 'skipped').length
const durationMs = suite?.endTime && suite?.startTime ? suite.endTime - suite.startTime : null

const fileKey = Object.keys(cov).find((k) => k !== 'total' && k.endsWith(HOOK_FILE)) ?? 'total'
const fileCov = cov[fileKey] ?? cov.total
const pct = (v) => (typeof v === 'number' ? `${v.toFixed(2)}%` : 'n/a')
const ratio = (m) => `${m.covered}/${m.total}`

// Compact uncovered line-range summary — read the v8 detail file if present.
let uncoveredRanges = 'n/a'
try {
  const detail = JSON.parse(readFileSync(join(COV_DIR, 'coverage-final.json'), 'utf8'))
  const entry = Object.values(detail).find((e) => e.path?.endsWith(HOOK_FILE))
  if (entry?.statementMap && entry?.s) {
    const missed = Object.entries(entry.s)
      .filter(([, count]) => count === 0)
      .map(([id]) => entry.statementMap[id]?.start?.line)
      .filter((n) => typeof n === 'number')
      .sort((a, b) => a - b)
    if (missed.length) {
      const ranges = []
      let start = missed[0]
      let prev = missed[0]
      for (const line of missed.slice(1)) {
        if (line === prev + 1) {
          prev = line
        } else {
          ranges.push(start === prev ? `${start}` : `${start}-${prev}`)
          start = prev = line
        }
      }
      ranges.push(start === prev ? `${start}` : `${start}-${prev}`)
      uncoveredRanges = ranges.join(', ')
    } else {
      uncoveredRanges = 'none'
    }
  }
} catch {
  /* coverage-final.json not always emitted; leave as n/a */
}

const rows = assertions
  .map((a) => {
    const icon = a.status === 'passed' ? 'PASS' : a.status === 'failed' ? 'FAIL' : 'SKIP'
    const ms = typeof a.duration === 'number' ? `${Math.round(a.duration)}ms` : '—'
    return `| ${icon} | ${a.title.replace(/\|/g, '\\|')} | ${ms} |`
  })
  .join('\n')

const md = `# Reminder Cron Activation — Test & Coverage Report

_Auto-generated by \`scripts/reminder-cron-activation-report.mjs\`. Regenerate with_
\`bun run report:reminder-cron\`_. Commit the diff to track regressions over time._

- **Test file:** \`${TEST_FILE}\`
- **Under test:** \`${HOOK_FILE}\`
- **Generated:** ${new Date().toISOString()}

## Summary

| Metric | Value |
| --- | --- |
| Tests total | ${assertions.length} |
| Passed | ${passed} |
| Failed | ${failed} |
| Skipped | ${skipped} |
| Suite duration | ${durationMs !== null ? `${durationMs}ms` : 'n/a'} |

## Coverage (hook file only)

| Metric | Covered | % |
| --- | --- | --- |
| Statements | ${ratio(fileCov.statements)} | ${pct(fileCov.statements.pct)} |
| Branches | ${ratio(fileCov.branches)} | ${pct(fileCov.branches.pct)} |
| Functions | ${ratio(fileCov.functions)} | ${pct(fileCov.functions.pct)} |
| Lines | ${ratio(fileCov.lines)} | ${pct(fileCov.lines.pct)} |

**Uncovered statement lines:** ${uncoveredRanges}

## Test cases

| Result | Title | Duration |
| --- | --- | --- |
${rows}
`

writeFileSync(OUT_FILE, md)
console.log(`Wrote ${OUT_FILE}`)
console.log(
  `Tests: ${passed}/${assertions.length} passed | ` +
    `Stmts ${pct(fileCov.statements.pct)} | Branch ${pct(fileCov.branches.pct)} | ` +
    `Func ${pct(fileCov.functions.pct)} | Lines ${pct(fileCov.lines.pct)}`,
)
